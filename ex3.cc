#include <iostream>
using namespace std;

int main() {
    int a = 10;
    cout << "La variable inicial es: " << a << endl;
    a += 5;
    cout << "Aumentado 5: " << a << endl;
    a -= 3;
    cout << "Reduciendo 3: " << a << endl;
    a *= 2;
    cout << "Multiplicado por 2: " << a << endl;
    a /= 4;
    cout << "Dividido en 4: " << a << endl;
  return 0;
}
